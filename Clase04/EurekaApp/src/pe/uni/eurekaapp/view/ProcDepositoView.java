  private void txtCuentaKeyReleased(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_txtCuentaKeyReleased
    // Previa
		txtMoneda.setText("");
		txtSaldo.setText("");
		// ValidaciÃ³n
		String cuenta = txtCuenta.getText();
		if(cuenta.length() != 8){
			return;
		}
		String regex = "[0-9]+";
		if(!cuenta.matches(regex)){
			return;
		}
		// Proceso
		try {
			String data = controller.traerDatosCuenta(cuenta);
			if( ! data.isEmpty() ){
				String[] items = data.split("\\|");
				txtMoneda.setText(items[0]);
				txtSaldo.setText(items[1]);
			}
		} catch (Exception e) {
		}
  }//GEN-LAST:event_txtCuentaKeyReleased

  private void btnProcesarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnProcesarActionPerformed
    btnProcesar.setEnabled(false);
		lblReporte.setText("");
		try {
			// Datos
			String cuenta = txtCuenta.getText();
			double importe = Double.parseDouble(txtImporte.getText());
			// Proceso
			int contMov = controller.procDeposito(cuenta,importe);
			lblReporte.setText("Proceso ok. Movimiento nro. " + contMov);
			// Interfaz
			txtCuenta.setEnabled(false);
			txtImporte.setEnabled(false);
			btnLimpiar.setEnabled(true);
		} catch (Exception e) {
			btnProcesar.setEnabled(true);
			Mensaje.showError(rootPane, e.getMessage());
		}
  }//GEN-LAST:event_btnProcesarActionPerformed

