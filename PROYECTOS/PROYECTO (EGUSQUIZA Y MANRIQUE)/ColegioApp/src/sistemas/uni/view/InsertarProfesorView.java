    void Combo(){
        Connection cn = null;
        this.cboCurso.removeAllItems();
        try{
            cn = AccesoDB.getConnection();
            Statement Sent=cn.createStatement();
            ResultSet rs=Sent.executeQuery("SELECT * FROM CURSO");
            while(rs.next()){
                this.cboCurso.addItem(rs.getString("CURS_NOM"));
            }
        }catch(Exception e){
            JOptionPane.showMessageDialog(null,e);
        }
    }
    
    public int ValorCurso(){
        Connection cn = null;
        String curso = String.valueOf(cboCurso.getSelectedItem());
        int valor = 0;
        try{
            cn = AccesoDB.getConnection();
            Statement Sent=cn.createStatement();
            ResultSet rs=Sent.executeQuery("SELECT * FROM CURSO WHERE CURS_NOM = '"+curso+"'");
            while(rs.next()){
                valor = rs.getInt("CURS_ID");
            }
        }catch(Exception e){
            JOptionPane.showMessageDialog(null,e);
        }
        return valor;
    }
    
    
    
