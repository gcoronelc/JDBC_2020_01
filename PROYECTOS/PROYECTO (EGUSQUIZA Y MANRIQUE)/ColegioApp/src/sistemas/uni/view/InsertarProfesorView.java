    void Mostrar_Profesores(){
        DefaultTableModel tabla = new DefaultTableModel();
        Connection cn = null;

        tabla.addColumn("Codigo");
        tabla.addColumn("DNI");
        tabla.addColumn("Nombres");
        tabla.addColumn("Apellidos");
        tabla.addColumn("Fecha de Nacimiento");
        tabla.addColumn("Direccion");
        tabla.addColumn("Telefono");
        tabla.addColumn("Correo");
        tabla.addColumn("Curso");

        try {
            cn = AccesoDB.getConnection();
            //Connection con = obj.getConnection();
            Statement st = cn.createStatement();
            ResultSet rs = st.executeQuery("SELECT p.PROF_ID,p.PROF_DNI,p.PROF_NOM,p.PROF_APE,to_char(p.PROF_FEC,'dd-mm-yyyy'),\n" +
            " p.PROF_DIR,p.PROF_TEL,p.PROF_COR,c.CURS_NOM \n" +
            " from PROFESOR p inner join CURSO c ON p.CURS_ID = c.CURS_ID");

            String datos[] = new String[9];

            while (rs.next()) {
                datos[0] = rs.getString(1);
                datos[1] = rs.getString(2);
                datos[2] = rs.getString(3);
                datos[3] = rs.getString(4);
                datos[4] = rs.getString(5);
                datos[5] = rs.getString(6);
                datos[6] = rs.getString(7);
                datos[7] = rs.getString(8);
                datos[8] = rs.getString(9);

                tabla.addRow(datos);
            }

            jTable1.setModel(tabla);

        } catch (SQLException ex) {

        }
        }
    
