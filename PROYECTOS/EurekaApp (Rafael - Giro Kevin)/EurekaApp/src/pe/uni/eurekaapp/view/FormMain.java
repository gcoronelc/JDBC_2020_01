    private void btnRepoClienteActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnRepoClienteActionPerformed
        // TODO add your handling code here:
        Connection cn = null;
        try {
          // El objeto Connection
          cn = AccesoDB.getConnection();
          // Par√°metros
          // Cargar el logo
          String fileLogo = "/pe/uni/eurekaapp/img/logo.gif";
          InputStream isLogo = Class.class.getResourceAsStream(fileLogo);     
          Map<String,Object> pars = new HashMap<>();
          pars.put("LOGO", isLogo);
          // Cargando el archivo compilado
          String fileRepo = "/pe/uni/eurekaapp/report/repoClientes.jasper";
          InputStream isRepo = Class.class.getResourceAsStream(fileRepo);
          JasperReport jrRepo = (JasperReport) JRLoader.loadObject(isRepo);
          JasperPrint jpRepo = JasperFillManager.fillReport(jrRepo, pars, cn);

          // Visualizando el Reporte
          JasperViewer viewer = new JasperViewer(jpRepo, false);
          viewer.setTitle("REPORTE DE CLIENTES");
          viewer.setVisible(true);

        } catch (Exception e) {
            Mensaje.showError(rootPane, e.getMessage());
        } finally{
          try {
            cn.close();
          } catch (Exception e) {
          }
        }
    }//GEN-LAST:event_btnRepoClienteActionPerformed

